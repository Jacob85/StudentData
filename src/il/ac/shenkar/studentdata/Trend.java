package il.ac.shenkar.studentdata;

public class Trend
{
	private int id;
	private String trendName;
	
	
	
	public Trend() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTrendName() {
		return trendName;
	}
	public void setTrendName(String trendName) {
		this.trendName = trendName;
	}
	
	

}
